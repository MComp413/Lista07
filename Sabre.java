package EP;

public class Sabre {
	private String cor;
	private String cristal;
	private String punho;
	private Ser dono;
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getCristal() {
		return cristal;
	}
	public void setCristal(String cristal) {
		this.cristal = cristal;
	}
	public String getPunho() {
		return punho;
	}
	public void setPunho(String punho) {
		this.punho = punho;
	}
	public Ser getDono() {
		return dono;
	}
	public void setDono(Ser dono) {
		this.dono = dono;
	}
}
